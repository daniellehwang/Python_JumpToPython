# 사칙연산
a = 3
b = 4
c = a + b
print (c)

d = a * b 
print (d)

e = a / b
print (e)

# ** 연산자, x의 y제곱
a = 3
b = 4
c = a ** b
print (c)

# % 연산자, 나눗셈 후 나머지를 반환
a = 7 % 3
print (a)

b = 3 % 7
print (b)

# // 연산자, 나눗셈 후 몫을 반환
a = 7 / 4
print (a)

b = 7 // 4
print (b)